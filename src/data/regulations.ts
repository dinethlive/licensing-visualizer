// =============================================================================
//  REGULATIONS, SINGLE SOURCE OF TRUTH
//
//  Every numeric figure, threshold, deadline, and named requirement that any
//  component renders comes from THIS file. Components reference regulations by
//  `id` only, never hardcode a number elsewhere in the app.
//
//  Compliance moves fast. The update workflow is intentionally simple:
//
//    1. Edit the matching entry below (change `value`, bump `version`,
//       update `effectiveDate` and `lastVerified`, swap `sourceIds` if needed).
//    2. Append one row to `changelog.ts` describing what changed and why.
//    3. (Optional) bump the visualizer version in `package.json`.
//
//  See HOW_TO_UPDATE.md at the project root for the full procedure.
// =============================================================================

export type Jurisdiction = 'MGA' | 'UKGC' | 'CGA' | 'GRA' | 'NJ-DGE' | 'PA-PGCB' | 'LK' | 'GLOBAL'

export type RegStatus = 'in-force' | 'superseded' | 'pending' | 'transitional'

export interface Regulation<TValue = unknown> {
  id: string                       // stable identifier, components import this
  jurisdiction: Jurisdiction
  topic: string                    // free-form, used for grouping in the UI
  title: string                    // short human-readable label
  value: TValue                    // the number / object / list itself
  unit?: string                    // e.g. "EUR", "minutes", "%"
  effectiveDate: string            // ISO 8601 date the rule started applying
  status: RegStatus
  version: number                  // bump on every change (start at 1)
  sourceIds: string[]              // foreign keys into sources.ts
  scriptSection?: string           // e.g. "2.1", links back to the documentary script
  notes?: string                   // anything subtle the reader should know
  lastVerified: string             // ISO 8601, date a human last confirmed this is current
}

// ----- helpers --------------------------------------------------------------

export function getRegulation<T = unknown>(id: string): Regulation<T> | undefined {
  return REGULATIONS.find((r) => r.id === id) as Regulation<T> | undefined
}

export function regulationsByJurisdiction(j: Jurisdiction): Regulation[] {
  return REGULATIONS.filter((r) => r.jurisdiction === j)
}

export function regulationsByTopic(topic: string): Regulation[] {
  return REGULATIONS.filter((r) => r.topic === topic)
}

// Convenience: type-narrowed accessors (so component code doesn't `as` everywhere).
export const num = (id: string): number => {
  const r = getRegulation<number>(id)
  if (!r || typeof r.value !== 'number') throw new Error(`regulation ${id} not a number`)
  return r.value
}
export const str = (id: string): string => {
  const r = getRegulation<string>(id)
  if (!r || typeof r.value !== 'string') throw new Error(`regulation ${id} not a string`)
  return r.value
}

